    private void btnLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLoginMouseClicked
        // TODO add your handling code here:
        
        if(RoleCb.getSelectedItem().toString().equals("Seller"))
        {
        String Query = "select * from APP.SELLERTBL where SELNAME='"+Uid.getText()+"' and SELPASS='"+password.getText()+"'";
        try{
            Con = DriverManager.getConnection("jdbc:derby://localhost:1527/MartSystemDb");
            St = Con.createStatement();
            Rs = St.executeQuery(Query);
            if(Rs.next()){
                new Selling().setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Sai Id nhân viên hoặc mật khẩu");
            }
        }catch(SQLException ex){
           ex.printStackTrace(); 
        }
        }else{
            String Query = "select * from APP.ADMINTBL where ADMINNAME='"+Uid.getText()+"' and ADMINPASS='"+password.getText()+"'";
        try{
            Con = DriverManager.getConnection("jdbc:derby://localhost:1527/MartSystemDb");
            St = Con.createStatement();
            Rs = St.executeQuery(Query);
            if(Rs.next()){
                new Products().setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Sai Id Admin hoặc mật khẩu");
            }
        }catch(SQLException ex){
           ex.printStackTrace(); 
        }
        }
    }//GEN-LAST:event_btnLoginMouseClicked

